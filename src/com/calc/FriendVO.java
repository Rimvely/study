package com.calc;

public class FriendVO {

	private String userName;
	private String userAge;
	private String chkGender;
	private String[] chkIdtype;
	
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserAge() {
		return userAge;
	}
	public void setUserAge(String userAge) {
		this.userAge = userAge;
	}
	public String getChkGender() {
		return chkGender;
	}
	public void setChkGender(String chkGender) {
		this.chkGender = chkGender;
	}
	public String[] getChkIdtype() {
		return chkIdtype;
	}
	public void setChkIdtype(String[] chkIdtype) {
		this.chkIdtype = chkIdtype;
	}
	
}
	
